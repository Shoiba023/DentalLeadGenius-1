export function getBlogBySlug(slug: string): BlogArticle | undefined {
  return blogArticles.find(article => article.slug === slug);
}

export function getAllBlogSlugs(): string[] {
  return blogArticles.map(article => article.slug);
}

export function getRecentArticles(count: number = 3): BlogArticle[] {
  return [...blogArticles]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, count);
}

export function getRelatedArticles(currentSlug: string, count: number = 3): BlogArticle[] {
  const current = getBlogBySlug(currentSlug);
  if (!current) return getRecentArticles(count);
  
  return blogArticles
    .filter(article => article.slug !== currentSlug && article.category === current.category)
    .slice(0, count);
}
